-- 术语库表
CREATE TABLE sys_terminology (
    id INT AUTO_INCREMENT PRIMARY KEY,
    domain VARCHAR(100) NOT NULL COMMENT '技术领域',
    term VARCHAR(200) NOT NULL COMMENT '术语',
    definition TEXT COMMENT '定义',
    aliases VARCHAR(500) COMMENT '别名，逗号分隔',
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='术语库';

